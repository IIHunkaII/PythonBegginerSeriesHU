# ezek azok: + - * / // <--egesz szamos osztas ** <--expodenciális osztás (hatványozás) % <--modulo

szam1 = 5
szam2 = 10

print ('1', szam1 + szam2)                                                                                   # 1
print ('2', szam2 - szam1)      # elszamolasok                                                               # 2
print ('3', szam1 * szam2)                                                                                   # 3
print ('4', szam1 / szam2)                                                                                   # 4

print ('5', szam2 // szam1)     # egesz szamos osztas  // <--!!!                                             # 5
print ('6', szam2 / szam1)      # a consolon lathato h 2.0 lett a 2 helyett!                                 # 6

print ('7', szam1 ** szam2)     # hatvanyozas (exponenciális osztas) !TANULAST IGENYEL!                                       # 7

print('8', szam2 % szam1)       # MAGYARAZAT-A 10ben az 5 elfér 2szer, maradt a nulla,ezert az eredmeny 0    # 8
                                # (mindig a # maradekot irja ki)

