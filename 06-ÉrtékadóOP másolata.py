
# assignment operators - értékadó operatorok

# '=' ez egy értékadó op. Azt csináLja, hogy megnezi  a jobb oldali értéket és hozzáadja a bal oldali változóhoz
# az "=" jel egy funkció, egy függvény
# ezek is azok: += -= *= /= //= %= **=

szam = 10 # ennyit ért eredetileg
print(szam)

szam += 1
print(szam)
# így értsd: szám = szám + 1 (amit a 11. sorban tortent)


# EZEK MIND A CIKLUSOKNÁL LESZNEK FONTOSAK

szam -= 1   # visszaadtam az eredeti értékét
print(szam)

szam *= 40
print(szam)

